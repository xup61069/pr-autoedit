# ============================================================
# 冗詞規則
# ============================================================

# 無條件刪除的填充音(語氣詞,幾乎不可能有語意)
# 這些信心高,審閱時不需要下 marker
FILLERS_ALWAYS = ["嗯", "呃", "啊", "欸", "唉", "痾", "喔"]

# 有條件刪除的詞(可能是連接詞也可能是口頭禪)
# 只在「句首孤立」或「連續重複」時刪,其餘保留
# 這些信心低,審閱時會下 marker 讓你確認
FILLERS_CONDITIONAL = ["就是", "然後", "那個", "這個", "所以說", "對對對"]

# 有條件冗詞的信心值(越低代表越需要人工確認)
CONDITIONAL_CONFIDENCE = 0.6

# 語氣詞要不要「前後都有停頓」才敢刪(秒)。
#   0    = 不要求(建議,Whisper 用):看到就刪,剪得最乾淨。
#   0.1  = 要求前後各停 0.1 秒才刪。這是為 FunASR 這類「逐字輸出」引擎準備的
#          保護——它會把「好啊」拆成兩個字,不設限會把句尾的「啊」也刪掉。
# 註:Whisper 會把詞排得很密,設 0.1 幾乎等於「永遠不刪語氣詞」。
FILLER_PAUSE_SEC = 0.0

# 口頭禪(就是、然後…)算「句首孤立」的停頓門檻(秒)。
# 前一個詞距離超過這個秒數,就當成新句子的開頭語助詞而刪掉。
# 調小 = 刪更多口頭禪(但誤刪連接詞的機會也變高;這類刪除信心低,會下 marker 供你確認)。
FILLER_ISOLATED_GAP_SEC = 0.25

# ============================================================
# 交付方式
# ============================================================

# 交給 Premiere 的方式:
#   "baked" = 直接剪好(預設):照決策直接剪掉冗詞、處理靜音,
#             匯入就是剪完的成品。想改靈敏度(門檻、倍率…)不用重跑全部:
#             在面板改完設定按「重算剪輯」,幾秒產出一個新序列,舊的還在。
#   "live"  = 活專案:所有段落都保留、只切開並上顏色標籤
#             (靜音=粉紅、音樂=青綠、冗詞=紫),進 Premiere 再自己批次處理。
#             註:片段和標籤數量很多,長片在 Premiere 裡可能變卡,
#             實測後改為備選。
DELIVERY_MODE = "baked"

# ============================================================
# 靜音規則
# ============================================================

# 相鄰兩詞間隔超過這個秒數,視為靜音空隙
SILENCE_THRESHOLD_SEC = 1.2

# 停頓的處理方式,三選一:
#   "auto"   看畫面決定(建議):畫面在動 -> 快轉帶過、畫面靜止 -> 剪掉。
#            你默默示範操作的那幾秒(拉推桿、開選單、比對前後)沒有講話,
#            只聽聲音的話會被當成停頓剪掉,內容真的消失而且不容易發現。
#            靈敏度等細節見下面「畫面活動」那一段。
#   "speed"  一律快轉:什麼都不刪,只是把停頓壓縮。最保守。
#   "delete" 一律剪掉:最兇,但默默示範的畫面也會一起不見。
#
# 註:這三個是互斥的。以前「看畫面決定」是另一個獨立開關(MOTION_DETECT),
# 結果它會蓋掉這裡的選擇——你選了快轉,畫面靜止的停頓還是被剪掉,
# 而且審閱報告看不出來。併成一個選項之後,選什麼就是什麼。
SILENCE_ACTION = "auto"

# 若 SILENCE_ACTION="speed",快轉倍率
SILENCE_SPEED_FACTOR = 6.0

# 若 SILENCE_ACTION="speed",是否把快轉段的聲音抹成無聲。
# True = 看得到快轉畫面,但那段沒有聲音(避免加速造成的尖聲/花栗鼠音)
# False = 快轉段保留原聲(會變高音)
MUTE_SPEED_AUDIO = True

# 保護緩衝:靜音段前後各保留這麼多秒,避免切太緊把氣音也切掉
SILENCE_PADDING_SEC = 0.15

# ============================================================
# 音樂/音效保護
# ============================================================
# 教學片常會播一段音樂或音效給觀眾聽,那段沒有講話、卻不能被
# 當成靜音剪掉或快轉。開啟後會用音量能量把這種段落找出來保護。

# 是否啟用音樂/音效偵測
MUSIC_DETECT = True

# 靈敏度:比「底噪」高出多少分貝才算有聲音(音樂)。
# 數字越小越敏感(容易把呼吸聲、椅子聲也當成音樂留下來);
# 越大越保守(小聲的音樂可能漏掉)。一般 8~16 之間。
MUSIC_DB_ABOVE_FLOOR = 12.0

# 短於這個秒數的聲響不算音樂(咳嗽、滑鼠喀一聲之類)。
# 實測:設 0.4 太寬鬆,17 分鐘的片會抓到 300 多段「音樂」——鍵盤聲、滑鼠聲、
# 呼吸都被當音樂保護住剪不掉。真正要保護的示範音樂通常好幾秒,設 1.2 比較準。
# 這個數字同時也是下面 NOISE_TRIM 的分界:比這短又沒有詞 = 雜音。
MUSIC_MIN_SEC = 1.2

# 剪掉咳嗽、清喉嚨、滑鼠喀一聲這類「短促雜音」。
#
# 這些聲音沒有對應的詞、但很大聲,所以能量偵測會把它們看成「有聲音的段落」。
# 麻煩的是咳嗽幾乎都緊接在講完一句話之後(先講完才咳),距離前面的說話
# 不到零點幾秒,能量偵測會把咳嗽跟那段講話「縫」成同一個有聲區間;
# 縫完當然超過 MUSIC_MIN_SEC,於是咳嗽就跟著被當成示範音樂保護起來,
# 不管你把停頓剪得多兇都剪不掉它。
#
# 開啟後,判斷改看「落在停頓裡的那一小段」有多長:
#     短於 MUSIC_MIN_SEC 且沒有任何詞  -> 雜音,剪掉
#     夠長                             -> 示範音樂,照舊保護
# 這些刪除信心 0.85(低於 marker 門檻),所以每一個都會下 marker,
# 可以在報告或 Premiere 裡逐一確認有沒有誤剪。
#
# ⚠️ 剪不掉的兩種情況(能量看不出來,要自己處理):
#   1. 講話講到一半咳 —— 咳嗽跟人聲疊在一起,分不開;
#   2. 連續咳很久(超過 MUSIC_MIN_SEC)—— 長度上跟示範音樂沒有差別。
NOISE_TRIM = True

# ============================================================
# 畫面活動(只有 SILENCE_ACTION = "auto" 時才用得到)
# ============================================================
# 「看畫面決定」怎麼判斷畫面算不算在動。選了別的停頓處理方式時,
# 這一段整段沒有作用。

# 每秒取樣幾張畫面來比對。4 張已足夠抓到操作動作;調高只會變慢。
MOTION_SAMPLE_FPS = 4.0

# 靈敏度:相鄰兩張縮圖的平均亮度差超過多少算「畫面在動」(0~255)。
# 數字越小越敏感(連滑鼠移動都算),越大只認大動作。
#
# 實測(17 分鐘 4K 教學片,382 個靜音段共 6.4 分):
#     0.3 -> 47 段改為加速(1.6 分)
#     0.5 -> 36 段改為加速(1.3 分)   <- 預設
#     1.0 -> 16 段改為加速(0.8 分)
#     2.0 ->  8 段改為加速(0.4 分)
# 刻意偏敏感:誤判成加速的代價很小(1.3 分在 20 倍速下只佔時間軸 4 秒),
# 誤刪掉的示範內容卻救不回來。
MOTION_SENSITIVITY = 0.5

# 短於這個秒數的停頓不看畫面,一律快轉帶過。
# 主要是擋掉能量微剪挖出來的零點幾秒小停頓 —— 那些不管怎麼判都沒有意義,
# 轉成變速還會在 Premiere 產生大量細碎的變速片段(效能地雷)。
MOTION_MIN_SEC = 0.5

# ============================================================
# 重講偵測(說錯重來 -> 自動砍掉前一次)
# ============================================================
# 講到一半發現講錯、停一下重講一次——前面那次是廢的,但它有完整語音,
# 靜音剪輯和冗詞判定都刪不掉它。這裡用「相鄰兩句的文字相似度」抓出來。
#
# ⚠️ 這是唯一會刪掉「真正說話內容」的功能,誤判成本比刪靜音高。
# 所有這類刪除的信心值都很低 -> 一定會下 marker,請在報告或 Premiere 裡
# 逐一確認。
#
# 預設關閉。實測結論(4 支片、共 55 分鐘):
#   嚴格設定只抓到約 12 秒(而且都是「shelf shelf」這種同字結巴);
#   放寬到抓得多時,抓到的幾乎都是「排比句」而不是重講 ——
#   例如「左側是哪個位置。右側是哪個位置」「X軸一圈、Y軸一圈」,
#   這是教學口白的正常修辭,砍掉會直接破壞內容。
# 所以除非你自認很常講錯重來,否則開了效益不大。要開的話務必看報告確認。
RETAKE_DETECT = False

# 相似度門檻 0~1。要多像才算「同一句重講」。
# 調高(0.9)=只抓幾乎一字不差的重講,很保守;
# 調低(0.7)=連講法有點不同的重來也抓,但誤砍機會變高。
RETAKE_SIMILARITY = 0.8

# 至少要重複這麼多字才算重講(「好」「對」「來」這種重複太正常,一比就誤判)
RETAKE_MIN_CHARS = 4

# 一次最多砍掉這麼多字的重複(比這更長多半是誤判)
RETAKE_MAX_CHARS = 24

# 交界處要有這麼久的停頓才算重講(秒)。
# 真的講錯重來,講者會頓一下;要求這個小停頓可以擋掉大量
# 「正常重複用字」的誤判。設 0 = 不要求(抓最兇,但誤判會變多)。
RETAKE_BOUNDARY_GAP_SEC = 0.15

# 重講刪除的信心值。低於 MARKER_MAX_CONFIDENCE 才會下 marker 供你確認。
RETAKE_CONFIDENCE = 0.5

# ============================================================
# 能量微剪(把「講話段裡面」真正沒聲音的小停頓也剪掉)
# ============================================================
# 為什麼需要:辨識引擎給的「詞」會把時間撐滿,兩個詞之間往往沒有空隙,
# 停頓其實被包在詞的時間範圍「裡面」。只看詞間隔的話這些完全剪不到。
# 這一步改用「實際音量」再掃一次保留段,把沒聲音的地方挖掉。
# 實測:一支 17 分鐘的片可以再剪掉 2~3 分鐘,且不損失任何講話內容。

# 是否啟用能量微剪
MICRO_TRIM = True

# 至少要能剪掉這麼久(秒)才動手。太小會把講話的自然節奏剁碎。
MICRO_TRIM_MIN_SEC = 0.25

# 每個停頓的頭尾各留這麼多秒不剪,聽起來才不會像被掐住脖子。
# 覺得剪完太急促、換氣感消失 -> 調大;覺得還是拖 -> 調小。
MICRO_TRIM_KEEP_SEC = 0.06

# 比「說話音量」低幾分貝算沒聲音。調小=更嚴格(剪比較少)、
