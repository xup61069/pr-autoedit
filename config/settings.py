    """現在有哪些設定跟內建預設不一樣。

    刻意做成「呼叫當下才算」而不是載入時算好一份:程式跑到一半還可能再改
    設定(例如面板重算鈕用 --mode 覆寫交付方式),事先算死的清單會過期,
    害審閱報告把「其實一樣的項目」也列成有改動。"""
    g = globals()
    return sorted(_k for _k, _v in DEFAULTS.items()
                  if _k not in _META_KEYS and g.get(_k) != _v)
