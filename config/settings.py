MICRO_TRIM_DB_BELOW_SPEECH = 22.0

# 不要剪掉「整個詞」。輕聲的短字(你、它、的)音量本來就低,可能整個掉在
# 門檻以下;連整個詞一起剪會少一個字、字幕也缺字,聽起來像跳針。
# 實測代價只有幾秒,但可以讓微剪造成的掉字歸零,建議保持 True。
MICRO_TRIM_PROTECT_WORDS = True

